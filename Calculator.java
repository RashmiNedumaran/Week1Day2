package Week1Day2Assignment;

public class Calculator {

	public int add(int num1,int num2,int num3)
	{
		int sum = num1 + num2 + num3;
		return sum;
	}
	
	public int sub(int num1,int num2,int num3)
	{
		int sub = num1 - num2 - num3;
		return sub;
	}
	
	public double mul(double num1,double num2,double num3)
	{
		double product = num1 * num2 * num3;
		return product;
	}
	public float divide(float num1,float num2,float num3)
	{
		float division = num1 / num2 / num3;
		return division;
	}
	
}
